# argumentos da funcao print 

"""
por default o print adiciona uma quebra de linha ao fim da string.
Porem, isso pode ser altearado usando o argumento end=''

"""
print('Hello', end=' ')
print('World!')


'''
Também é possivel mudar o espaco adicionado por default entre 
elementos dentro da funcao print. Usando o argumento sep=''
'''

print('feijao', 'arroz', 'cafe', 'carne', sep=' - ')

